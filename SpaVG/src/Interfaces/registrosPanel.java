    public boolean registrarCorteDiario(Connection con, LocalDate fecha, double fondoInicial, double gananciasDelDia, double total) {
    String sql = "INSERT INTO CortesDiarios (fecha, fondoInicial, gananciasDelDia, total) VALUES (?, ?, ?, ?)";
    
    try (PreparedStatement pst = con.prepareStatement(sql)) {
        pst.setDate(1, java.sql.Date.valueOf(fecha));
        pst.setDouble(2, fondoInicial);
        pst.setDouble(3, gananciasDelDia);
        pst.setDouble(4, total);
        
        int filasAfectadas = pst.executeUpdate();
        return filasAfectadas > 0; // Retornar true si se insertó al menos una fila
    } catch (SQLException e) {
        e.printStackTrace();
        return false; // Retornar false si ocurre un error durante la inserción
    }
}
    
