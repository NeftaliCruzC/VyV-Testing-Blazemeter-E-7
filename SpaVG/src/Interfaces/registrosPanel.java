    public void cargarDatosCorte() {
    try {
        // Obtenemos la fecha actual
        java.sql.Date fechaActual = new java.sql.Date(System.currentTimeMillis());

        // Consulta para obtener el fondo y las ganancias del día según la fecha actual
        String sql = "SELECT fondoInicial, gananciasDelDia, total FROM CortesDiarios WHERE fecha = ?";
        PreparedStatement pst = cn.prepareStatement(sql);
        pst.setDate(1, fechaActual);

        ResultSet rs = pst.executeQuery();

        if (rs.next()) {
            BigDecimal fondoInicial = rs.getBigDecimal("fondoInicial");
            BigDecimal gananciasDia = rs.getBigDecimal("gananciasDelDia");
            BigDecimal total = rs.getBigDecimal("total");

            // Actualizar los text fields en el registrosPanel
            txtFechaGanancia.setText(fechaActual.toString());
            txtFondoActual.setText(fondoInicial.toString());
            txtGananciasDia.setText(gananciasDia.toString());
            txtTotalDia.setText(total.toString());
        } else {
            // Si no hay registro para la fecha actual, se inicializan los campos con cero
            txtFechaGanancia.setText(fechaActual.toString());
            txtFondoActual.setText("0.00");
            txtGananciasDia.setText("0.00");
            txtTotalDia.setText("0.00");
        }

        rs.close();
        pst.close();
    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(null, "Error al cargar los datos del corte: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
}



    
    
    
    
